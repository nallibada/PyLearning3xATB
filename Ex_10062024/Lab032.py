# Ternary Operator :- writing an if else condition in a single line and print the output
pramod_marks = 100
amit_marks = 50
print("Pramod got more marks" if pramod_marks > amit_marks else "Amit got more marks")

if pramod_marks > amit_marks:
    print("Pramod got more marks")
else:
    print("Amit got more marks")